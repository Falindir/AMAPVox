package fr.ird.voxelidar.engine3d.math.point;

/**
 *
 * @author Julien
 */
public class Point2F {
    
    public float x, y;

    public Point2F() {
        this.x = 0;
        this.y = 0;
    }
    
    public Point2F(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
